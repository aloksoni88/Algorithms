package com.hashing;

public class StuentWithoutEquNHash {
	private int rollNo;
	private String name;
	private float marks;
	
	public StuentWithoutEquNHash(int rollNo, String name, float marks) {
		this.rollNo = rollNo;
		this.name = name;
		this.marks = marks;
	}
	
	
	
	public int getRollNo() {
		return rollNo;
	}



	public String getName() {
		return name;
	}



	public float getMarks() {
		return marks;
	}
}
